        <div class="container cuerpo">
          <h5 class="text-center dispociones">DE LA ADMINISTRACION Y REQUISITOS</h5>
          <p class="text-justify"><strong>Artículo 11°.-</strong> La Unidad de Servicio Social considerará como elemento prioritario de evaluación, la situación socioeconómica y luego el rendimiento académico, considerándose dentro de esta evaluación aquellos <mark>alumnos matriculados en 16 créditos.</mark></p>
          
          <h5 class="text-center dispociones">DE LOS SERVICIOS Y ATENCIÓN</h5>
          <p class="text-justify"><strong>Artículo 16°.-</strong> El <mark>alumno perderá la condición de usuario</mark> de la Asistencia Alimentaria(comedor universitario), si comete actos de indisciplina y reñidos contra la moral, en agravio de los estudiantes, de los responsables del control del comedor universitario, autoridades, docentes y personal administrativo.</p>

          <p class="text-justify"><strong>Artículo 19°.-</strong> El carné del servicio de Comedor Universitario(Carné Universitario) es intransferible, deberá ser usado exclusivamente por el beneficiario. Una vez publicada la nómina de postulantes aceptados, <mark>se concederá un plazo máximo de diez (10) días para que el estudiante haga uso de la plaza otorgada, pasado el tiempo señalado y de no haber hecho efectivo el beneficio, será automáticamente anulado</mark> y ocupará la vacante el estudiante que continua en el orden de selección.</p>

          <p class="text-justify"><strong>Artículo 20°.-</strong> Las vacantes otorgadas tendrán <mark>vigencia de un semestre académico</mark>, concluido el mismo, el estudiante deberá renovar su solicitud, actualizar los requisitos y documentos necesarios, señalados para este efecto.</p>
          
          <h5 class="text-center dispociones">DEL MENÚ Y LA ATENCIÓN</h5>
          <p class="text-justify"><strong>Artículo 23°.-</strong> El beneficiario deberá <mark>respetar el horario del comedor</mark>; así como el control de su tarjeta para poder recibir la ración correspondiente.</p>

          <p class="text-justify"><strong>Artículo 24°.-</strong> El beneficiario acercará su bandeja al servidor para solicitar las raciones que le corresponda consumir, <mark>evitando el desperdicio de las mismas.</mark></p>

          <p class="text-justify"><strong>Artículo 28°.-</strong> La atención del comedor se suspenderá los días sábados, domingos y feriados.</p>

          <h5 class="text-center dispociones">SANCIONES Y TERMINO DE LA CONDICION DE COMENSAL</h5>
          <p class="text-justify" style="margin-bottom: 4px;"><strong>Artículo 32°.-</strong> Son Faltas que conducen a amonestación o suspensión a los alumnos comensales, lo siguiente:</p>
            <ul class="list-missing">
              <li><mark>Por inasistencia injustificada hasta por 03 días consecutivos a la suma de 5 días en 01 mes sin justificación.</mark></li>
              <li>Por vender, transferir, adulterar o prestar los carnés de comensal a terceras personas.</li>
              <li>Por faltar verbal o físicamente a los comensales y/o servidores del comedor.</li>
              <li>Por sustraer o deteriorar deliberadamente los bienes del comedor.</li>
              <li>Por concurrir al comedor en estado etílico bajo influencias de drogas o alcohol.</li>
              <li>Otros que señale las normas internas de la Universidad.</li>
            </ul>
          <p class="text-justify" style="margin-bottom: 4px;"><strong>Artículo 34°.-</strong> Serán sancionados con suspensión de tres días los estudiantes que incurran en cualquiera de las siguientes faltas:</p>
            <ul class="list-missing">
              <li>Exigir atención en forma irrespetuosa, con mayor razón si esto ocurre fuera del horario establecido.</li>
              <li>Faltar de palabra u obra a los servidores del comedor o a sus compañeros.</li>
              <li>Ingresar al área de comensales con animales vivos de cualquier especie.</li>
              <li>Presentarse sin el adecuado aseo personal.</li>
              <li>Negarse a presentar, cuando se requiera su carné universitario u otro documento de identificación.</li>
              <li>Incitar o promover dentro del comedor, discusiones públicas o manifestaciones de carácter político o de cualquier otro género.</li>
              <li>Provocar desórdenes.</li>
            </ul>
          <p class="text-justify" style="margin-bottom: 4px;"><strong>Artículo 38°.-</strong> Son aplicables las siguientes sanciones:</p>
            <ul class="list-missing">
              <li>Amonestación verbal o escrita.</li>
              <li>Suspensión temporal.</li>
              <li>Suspensión definitiva.</li>
            </ul>
